"""**AST parsing utilities**"""

def analyze_ast(code: str) -> Dict[str, Any]:
    tree = ast.parse(code)
    functions, classes, imports, from_imports = [], [], [], []
    loops, conditionals, returns, raises = 0,0,0,0
    recursion = False
    called_funcs = set()

    for node in ast.walk(tree):
        if isinstance(node, ast.FunctionDef):
            functions.append(node.name)
        elif isinstance(node, ast.ClassDef):
            classes.append(node.name)
        elif isinstance(node, ast.Import):
            for n in node.names: imports.append(n.name)
        elif isinstance(node, ast.ImportFrom):
            mod = node.module if node.module else ""
            for n in node.names: from_imports.append(f"{mod}.{n.name}".strip("."))
        elif isinstance(node, (ast.For, ast.While)):
            loops += 1
        elif isinstance(node, ast.If):
            conditionals += 1
        elif isinstance(node, ast.Return):
            returns += 1
        elif isinstance(node, ast.Raise):
            raises += 1
        elif isinstance(node, ast.Call):
            if isinstance(node.func, ast.Name):
                called_funcs.add(node.func.id)

    # simple recursion check: any function calls itself
    recursion = any(fn in called_funcs for fn in functions)

    return {
        "functions": functions,
        "classes": classes,
        "imports": imports,
        "from_imports": from_imports,
        "loops": loops,
        "ifs": conditionals,
        "returns": returns,
        "raises": raises,
        "recursion": recursion,
    }

