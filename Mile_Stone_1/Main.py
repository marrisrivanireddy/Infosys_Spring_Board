"""**Tokenization**"""

def tokenize_code(code: str) -> Dict[str, Any]:
    toks = []
    counts = {}
    for tok in tokenize.generate_tokens(io.StringIO(code).readline):
        toks.append(tok.string)
        counts[tok.type] = counts.get(tok.type, 0) + 1
    joined = " ".join([t for t in toks if t.strip()])
    return {"tokens": toks, "joined": joined, "count": len(toks), "types": counts}

token_reports = [tokenize_code(c) for c in code_snippets]
