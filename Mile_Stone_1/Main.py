"""**PCA**"""

def pca_plot(E: np.ndarray, title: str):
    p = PCA(n_components=2).fit_transform(E)
    plt.figure()
    plt.scatter(p[:,0], p[:,1])
    for i, (x,y) in enumerate(p):
        plt.annotate(str(i+1), (x, y))
    plt.title(title)
    plt.xlabel("PC1"); plt.ylabel("PC2")
    plt.show()

for label, E in embeddings.items():
    pca_plot(E, f"PCA — {label}")

