"""**Cross** -**model** **comparison**"""

def upper_tri_flat(M: np.ndarray) -> np.ndarray:
    n = M.shape[0]
    idx = np.triu_indices(n, k=1)
    return M[idx]

labels = list(sims.keys())
comparisons = []
for a, b in itertools.combinations(labels, 2):
    va, vb = upper_tri_flat(sims[a]), upper_tri_flat(sims[b])
    pear = np.corrcoef(va, vb)[0,1]
    comparisons.append((a, b, float(pear)))

print("Pearson correlation between similarity matrices:")
for a,b,r in comparisons:
    print(f"{a} vs {b}: {r:.3f}")

